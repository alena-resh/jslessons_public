let n = 10;
let str = ''
for (let i = 1; i <= n; i++)
{
str += ' '.repeat(n - i);
for (let j = 1; j <= i; j++)
{
    str += j % 10
}
for (let k = i - 1; k >= 1; k--)
{
    str += k 
}
str += '\n'
}
for (let i = 1; i <= n - 1; i++) // reverted
{
str += ' '.repeat(n - (n - i));
for (let j = 1; j <= n - i; j++)
{
    str += j 
}
for (let k = n - i - 1; k >= 1; k--)
{
    str += k
}
str += '\n'
}
